CREATE TABLE TB_ITEM
(
   ID_ITEM              INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
   ID_AUTOR             INTEGER,
   ID_GENERO            INTEGER,
   ID_EDITORA           INTEGER,
   TITULO               VARCHAR(255) NOT NULL,
   ANO_PUBLICACAO       INTEGER,
   IND_TIPO             VARCHAR(1) NOT NULL,
   QTD_DISPONIVEL       INTEGER NOT NULL,
   IND_DISPONIVEL       VARCHAR(1),
   CONSTRAINT FK_TB_ITEM_TB_AUTOR FOREIGN KEY (ID_AUTOR) REFERENCES TB_AUTOR (ID_AUTOR),
   CONSTRAINT FK_TB_ITEM_TB_GENERO FOREIGN KEY (ID_GENERO) REFERENCES TB_GENERO (ID_GENERO),
   CONSTRAINT FK_TB_ITEM_TB_EDITORA FOREIGN KEY (ID_EDITORA) REFERENCES TB_EDITORA (ID_EDITORA)
);