CREATE TABLE TB_EMPRESTIMO
(
   ID_EMPRESTIMO           INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
   ID_ITEM                 INTEGER NOT NULL,
   ID_USUARIO              INTEGER NOT NULL,
   DTH_EMPRESTIMO          TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
   DTH_DEVOLUCAO_PREVISTA  DATE NOT NULL,
   DAT_DEVOLUCAO_REAL      DATE,
   VALOR_MULTA             DECIMAL(10,2),
   DTH_MULTA               TIMESTAMP,
   CONSTRAINT FK_TB_EMPRESTIMO_TB_ITEM FOREIGN KEY (ID_ITEM) REFERENCES TB_ITEM (ID_ITEM) ON DELETE RESTRICT ON UPDATE CASCADE,
   CONSTRAINT FK_TB_EMPRESTIMO_TB_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES TB_USUARIO (ID_USUARIO) ON DELETE RESTRICT ON UPDATE CASCADE
);